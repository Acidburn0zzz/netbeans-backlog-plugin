package com.junichi11.netbeans.modules.backlog.utils;

import org.junit.After;
import org.junit.AfterClass;
import org.junit.Before;
import org.junit.BeforeClass;
import org.junit.Test;
import static org.junit.Assert.*;

/**
 *
 * @author junichi11
 */
public class BacklogUtilsTest {

    public BacklogUtilsTest() {
    }

    @BeforeClass
    public static void setUpClass() {
    }

    @AfterClass
    public static void tearDownClass() {
    }

    @Before
    public void setUp() {
    }

    @After
    public void tearDown() {
    }

    /**
     * Test of isDateFormat method, of class BacklogUtils.
     */
    @Test
    public void testIsDateFormat() {
        assertTrue(BacklogUtils.isDateFormat("2005/02/08"));

        assertFalse(BacklogUtils.isDateFormat(""));
        assertFalse(BacklogUtils.isDateFormat(null));
        assertFalse(BacklogUtils.isDateFormat("test"));
        assertFalse(BacklogUtils.isDateFormat("100/02/08"));
        assertFalse(BacklogUtils.isDateFormat("25/02/08"));
        assertFalse(BacklogUtils.isDateFormat("5/02/08"));
        assertFalse(BacklogUtils.isDateFormat("2005-02-08"));
        assertFalse(BacklogUtils.isDateFormat("2005/02/30"));
        assertFalse(BacklogUtils.isDateFormat("2014/10/18aaa"));
        assertFalse(BacklogUtils.isDateFormat("aaa2014/10/18"));
    }

}
